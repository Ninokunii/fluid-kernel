static void put_pixel(int x, int y, u8 color) {
    if ((unsigned)x >= FB_WIDTH || (unsigned)y >= FB_HEIGHT) return;
    FB_ADDR[y * FB_WIDTH + x] = color;
}

void fb_text_n(int x, int y, const char *s, int n, u8 color) {
    for (int i = 0; i < n; ++i) {
        char c = s[i];
        if (!c) break;
        if (c == '\n') { y += 9; x = 0; continue; }
        for (int row = 0; row < 7; ++row) {
            u8 bits = glyph_row(c, row);
            for (int col = 0; col < 5; ++col) {
                if (bits & (1 << (4 - col))) put_pixel(x + col, y + row, color);
            }
        }
        x += 6;
    }
}

void fb_text(int x, int y, const char *s, u8 color) {
    int n = 0;
    while (s[n]) ++n;
    fb_text_n(x, y, s, n, color);
}
